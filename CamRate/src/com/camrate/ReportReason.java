package com.camrate;

public class ReportReason {

	private String di;
	private String RR_ID;
	private String RR_Reason;
	private String RR_For;

	public String getDi() {
		return di;
	}

	public void setDi(String di) {
		this.di = di;
	}

	public String getRR_ID() {
		return RR_ID;
	}

	public void setRR_ID(String rR_ID) {
		RR_ID = rR_ID;
	}

	public String getRR_Reason() {
		return RR_Reason;
	}

	public void setRR_Reason(String rR_Reason) {
		RR_Reason = rR_Reason;
	}

	public String getRR_For() {
		return RR_For;
	}

	public void setRR_For(String rR_For) {
		RR_For = rR_For;
	}

}
